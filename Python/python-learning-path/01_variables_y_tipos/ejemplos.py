# Ejemplos de variables y tipos

# Variable de tipo entero
numero = 10
print("Número:", numero, "Tipo:", type(numero))

# Variable de tipo float
decimal = 3.14
print("Decimal:", decimal, "Tipo:", type(decimal))

# Variable de tipo string
texto = "Python"
print("Texto:", texto, "Tipo:", type(texto))

# Variable de tipo booleano
activo = False
print("Activo:", activo, "Tipo:", type(activo))

