# Ejemplos de condicionales

# Condicional simple
numero = -5
if numero > 0:
    print("El número es positivo")
elif numero < 0:
    print("El número es negativo")
else:
    print("El número es cero")

# Condicional con edad
edad = 20
if edad >= 18:
    print("Acceso permitido")
else:
    print("Acceso denegado")

# Mini reto: calificación
calificacion = 7
if calificacion >= 6:
    print("Aprobado")
else:
    print("Reprobado")

