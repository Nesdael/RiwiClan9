# Ejemplos de ciclos

# Ciclo for
for i in range(1, 11):
    print("Número:", i)

# Ciclo while
num = 0
while num <= 5:
    print("Número ingresado:", num)
    num += 1

# Uso de break y continue
for n in range(10):
    if n % 2 != 0:
        continue  # Solo pares
    print("Par:", n)
    if n == 8:
        break  # Termina en 8

