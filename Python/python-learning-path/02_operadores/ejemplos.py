# Ejemplos de operadores

# Operadores matemáticos
x = 10
y = 3
print("Suma:", x + y)
print("Resta:", x - y)
print("Multiplicación:", x * y)
print("División:", x / y)
print("División entera:", x // y)
print("Módulo:", x % y)
print("Potencia:", x ** y)

# Operadores de comparación
print("¿x es igual a y?", x == y)
print("¿x es diferente de y?", x != y)
print("¿x es mayor que y?", x > y)
print("¿x es menor o igual que y?", x <= y)

# Operadores lógicos
mayor_de_edad = True
es_estudiante = False
print("¿Mayor de edad y estudiante?", mayor_de_edad and es_estudiante)
print("¿Mayor de edad o estudiante?", mayor_de_edad or es_estudiante)
print("No es estudiante:", not es_estudiante)

