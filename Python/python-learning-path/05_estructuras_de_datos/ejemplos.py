# Ejemplos de estructuras de datos

# Lista
colores = ["rojo", "verde", "azul"]
print("Primer color:", colores[0])

# Tupla
dias = ("lunes", "martes", "miércoles")
print("Segundo día:", dias[1])

# Diccionario
auto = {"marca": "Toyota", "modelo": 2020}
print("Marca del auto:", auto["marca"])

# Set
letras = {"a", "b", "c", "a"}
print("Letras únicas:", letras)

